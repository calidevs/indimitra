// Function to get a random image based on category
export const getRandomGroceryImage = (category) => {
  // If category exists in our collection, get a random image from that category
  if (category && INDIAN_GROCERY_IMAGES[category.toLowerCase()]) {
    const images = INDIAN_GROCERY_IMAGES[category.toLowerCase()];
    return images[Math.floor(Math.random() * images.length)];
  }

  // If category doesn't exist, return default image
  return INDIAN_GROCERY_IMAGES.default;
};

// Function to get a fixed image based on category and index
export const getFixedGroceryImage = (category, index = 0) => {
  // If category exists in our collection, get the specified image from that category
  if (category && INDIAN_GROCERY_IMAGES[category.toLowerCase()]) {
    const images = INDIAN_GROCERY_IMAGES[category.toLowerCase()];
    return images[index % images.length]; // Use modulo to handle out of bounds
  }

  // If category doesn't exist, return default image
  return INDIAN_GROCERY_IMAGES.default;
};
